void solve()
{ 
    ll n,k,res=0;
        cin>>n>>k;
        vector<ll>v(n+1),range(2*k+10),sum(2*k+10);
        for(int i=1;i<=n;i++){
            cin>>v[i];
        }
        for(int i=1;i<=n/2;i++){
            ll a=v[i],b=v[n-i+1];
            range[min(a,b)+1]+=1;
            range[max(a,b)+k+1]-=1;
            sum[a+b]+=1;
        }
        for(int i=1;i<=2*k;i++){
            range[i]+=range[i-1];
            res=max(res,range[i]+sum[i]);
        }
        cout<<n-res<<endl;
}




